    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        Usuario u = new Usuario();

        NegocioUsuario negU = new NegocioUsuario();

        ArrayList<Usuario> usuarios = negU.getUsuarios();

        for (int i = 0; i < usuarios.size(); i++) {
            if (this.txtUser.getText().equals(usuarios.get(i).getUser()) && this.txtPass.getText().equals(usuarios.get(i).getPass())) {
                u = usuarios.get(i);
            }
                    }
        if (u.getNivel()==0) {
            JOptionPane.showMessageDialog(this, "No se encuentra el usuario", "Error de ingreso", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            MenuPrincipal m = new MenuPrincipal(u.getNivel());
            m.setLocationRelativeTo(null);
            m.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

